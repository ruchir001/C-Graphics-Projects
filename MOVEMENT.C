#include<stdio.h>
#include<conio.h>
#include<dos.h>
#include<graphics.h>
void main()
{
int r=10,c=25,x=0;
clrscr();
gotoxy(c,r);
printf("*");
while(x!=13)
{
x=getch();
if(x==72)
{
if(r>1)
{
clrscr();
gotoxy(c,--r);
printf("*");

}
}
if(x==75)
{
if(c>1)
{
clrscr();
gotoxy(--c,r);
printf("*");
}
}
if(x==80)
{
if(r<=35)
{
clrscr();
gotoxy(c,++r);
printf("*");
}
}
if(x==77)
{
if(c<90)
{
clrscr();
gotoxy(++c,r);
printf("*");
}
}
}
getch();
}
