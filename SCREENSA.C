#include<stdio.h>
#include<conio.h>
#include<dos.h>
#include<graphics.h>
void main()
{
int c=20,r=40,gd,gm;
char *bm;
clrscr();
gd=DETECT ;
initgraph(&gd,&gm,"c://tc//bgi");
setcolor(YELLOW);
circle(60,80,36);
outtextxy(25,80,"V.I.B.O.R");
bm=malloc(imagesize(20,40,100,120));
getimage(20,40,100,120,bm);
for(r=20;r<=400;r+=2)
{
c=c+1;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
for(r;r>=100;r--)
{
c=c+1;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
for(r;r>=1;r--)
{
c=c-1;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
for(c;c>1;c-=2)
{
r=r+1;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
for(r;r<=400;r++)
{
c=c+1;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
for(c;c<514;c+=2)
{
r=r-1;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
for(r;r>=40;r--)
{
c=c-2;
putimage(c,r,bm,COPY_PUT);
delay(10);
}
getch();
}

